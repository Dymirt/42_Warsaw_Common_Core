void	*ft_memchr(const void *s, int c, int n)
{
	char	*p;
	int		i;

	p = (char *)s;
	i = 0;
	while (i < n)
	{
		if (p[i] == (char)c)
			return ((void *)(p + i));
		i++;
	}
	return (0);
}
