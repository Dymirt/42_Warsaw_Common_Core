void *memchr(const void *s, int c, int n)
{
	int i;
	i = 0;
	while (i != n)
	{
		if (s + 1 == c)
			return ((void *)s + i);
		i++;
	}
	if (c == '\0')
		return ((char *)s + i);
	return (0);
}
